package Models.Objects;

public class Game {
	
	private String gameName;
	private double gameCost;
	private String gameReleaseDate;
	private String gameMainPic;
	private String gameCompany;
	private String gameYouTubeTrailer;
	private String gameSeriesName;
	private String gameRequirementId;
	
	public String getGameName() {
		return gameName;
	}
	
	public void setGameName(String gameName) {
		this.gameName = gameName;
	}
	
	public double getGameCost() {
		return gameCost;
	}
	
	public void setGameCost(double gameCost) {
		this.gameCost = gameCost;
	}
	
	public String getGameReleaseDate() {
		return gameReleaseDate;
	}
	
	public void setGameReleaseDate(String gameReleaseDate) {
		this.gameReleaseDate = gameReleaseDate;
	}
	
	public String getGameMainPic() {
		return gameMainPic;
	}
	
	public void setGameMainPic(String gameMainPic) {
		this.gameMainPic = gameMainPic;
	}
	
	public String getGameCompany() {
		return gameCompany;
	}
	
	public void setGameCompany(String gameCompany) {
		this.gameCompany = gameCompany;
	}
	
	public String getGameYouTubeTrailer() {
		return gameYouTubeTrailer;
	}
	
	public void setGameYouTubeTrailer(String gameYouTubeTrailer) {
		this.gameYouTubeTrailer = gameYouTubeTrailer;
	}
	
	public String getGameSeriesName() {
		return gameSeriesName;
	}
	
	public void setGameSeriesName(String gameSeriesName) {
		this.gameSeriesName = gameSeriesName;
	}
	
	public String getGameRequirementId() {
		return gameRequirementId;
	}
	
	public void setGameRequirementId(String gameRequirementId) {
		this.gameRequirementId = gameRequirementId;
	}
	
}
