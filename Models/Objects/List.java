package Models.Objects;

public class List {
	
	private int listId;
	private String listName;
	private String listNumberOfGames;
	
	public int getListId() {
		return listId;
	}
	
	public void setListId(int listId) {
		this.listId = listId;
	}
	
	public String getListName() {
		return listName;
	}
	
	public void setListName(String listName) {
		this.listName = listName;
	}
	
	public String getListNumberOfGames() {
		return listNumberOfGames;
	}
	
	public void setListNumberOfGames(String listNumberOfGames) {
		this.listNumberOfGames = listNumberOfGames;
	}
	
}
