package Models.Objects;

public class Series {
	
	private String seriesName;
	private int seriesNumberOfGames;
	
	public String getSeriesName() {
		return seriesName;
	}
	
	public void setSeriesName(String seriesName) {
		this.seriesName = seriesName;
	}
	
	public int getSeriesNumberOfGames() {
		return seriesNumberOfGames;
	}
	
	public void setSeriesNumberOfGames(int seriesNumberOfGames) {
		this.seriesNumberOfGames = seriesNumberOfGames;
	}
}
